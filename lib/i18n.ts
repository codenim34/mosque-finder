type MessageKey = keyof (typeof messages)['bn']

export function t(locale: Locale, key: MessageKey, vars?: Record<string, string | number>) {
  const template = messages[locale][key] ?? messages.en[key]

  if (!vars) {
    return template
  }

  return Object.entries(vars).reduce((text, [name, value]) => {
    return text.replace(new RegExp(`{{${name}}}`, 'g'), String(value))
  }, template)
}

